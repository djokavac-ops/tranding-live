def regime_shift_detection():
    rows=q("SELECT * FROM market_snapshots ORDER BY id DESC LIMIT 20", fetch=True)
    if len(rows)<5:
        return {"shift_detected":False,"confidence":0,"message":"Not enough snapshots yet."}
    recent=rows[:5]
    older=rows[5:15] if len(rows)>=15 else rows[5:]
    recent_wait=sum(r.get("wait_count") or 0 for r in recent)/max(1,len(recent))
    older_wait=sum(r.get("wait_count") or 0 for r in older)/max(1,len(older))
    recent_avg=sum(float(r.get("avg_confidence") or 0) for r in recent)/max(1,len(recent))
    older_avg=sum(float(r.get("avg_confidence") or 0) for r in older)/max(1,len(older))
    shift=abs(recent_avg-older_avg)>12 or abs(recent_wait-older_wait)>5
    return {"shift_detected":shift,"confidence":round(min(100,abs(recent_avg-older_avg)*4+abs(recent_wait-older_wait)*5),1),"recent_avg_confidence":round(recent_avg,1),"older_avg_confidence":round(older_avg,1),"message":"Regime shift detected." if shift else "No major regime shift detected."}

