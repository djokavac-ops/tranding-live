class BrokerAdapter:
    name="base"
    def status(self):
        return {"name":self.name,"configured":False,"live_ready":False}
    def validate_order(self, order):
        required=["symbol","side","qty","entry","stop","target1"]
        missing=[k for k in required if k not in order or order.get(k) is None]
        if missing:
            return {"ok":False,"errors":[f"Missing {x}" for x in missing]}
        if order["side"] not in ("BUY","SELL"):
            return {"ok":False,"errors":["Invalid side"]}
        if float(order["qty"]) <= 0:
            return {"ok":False,"errors":["Qty must be positive"]}
        return {"ok":True,"errors":[]}
    def place_order(self, order):
        return {"ok":False,"status":"not_implemented","message":"Adapter not implemented"}

