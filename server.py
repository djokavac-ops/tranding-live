def confidence_calibration():
    trades=closed_trades_for_symbol()
    buckets={"70-79":{"n":0,"wins":0},"80-89":{"n":0,"wins":0},"90-100":{"n":0,"wins":0}}
    for t in trades:
        score=float(t.get("ai_score") or t.get("confidence") or 0)
        pnl=float(t.get("pnl") or 0)
        key="90-100" if score>=90 else "80-89" if score>=80 else "70-79" if score>=70 else None
        if key:
            buckets[key]["n"]+=1
            if pnl>0: buckets[key]["wins"]+=1
    for k,d in buckets.items():
        d["win_rate"]=round(d["wins"]/d["n"]*100,1) if d["n"] else None
        if d["n"] and k=="90-100" and d["win_rate"]<55:
            d["recommendation"]="tighten high-confidence threshold"
        elif d["n"]:
            d["recommendation"]="observe"
        else:
            d["recommendation"]="not enough data"
    return {"buckets":buckets,"message":"Calibration compares AI confidence buckets with actual paper outcomes."}

