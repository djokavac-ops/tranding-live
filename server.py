def recent_closed_trades(limit=10):
    return q("SELECT * FROM paper_trades WHERE status!='OPEN' ORDER BY id DESC LIMIT :limit", {"limit":limit}, fetch=True)

def loss_streak():
    rows=recent_closed_trades(20)
    streak=0
    for t in rows:
        if float(t.get("pnl") or 0) < 0:
            streak += 1
        else:
            break
    return streak

def cooldown_status():
    s=get_settings()
    needed=int(s.get("cooldown_after_losses",2))
    hours=float(s.get("cooldown_hours",6))
    streak=loss_streak()
    if streak < needed:
        return {"active":False,"loss_streak":streak,"needed":needed,"message":"No cooldown active."}
    rows=q("SELECT * FROM paper_trades WHERE status!='OPEN' ORDER BY id DESC LIMIT 1", fetch=True)
    if not rows:
        return {"active":False,"loss_streak":streak,"needed":needed,"message":"No closed trades."}
    # Simple conservative behavior: when streak threshold is reached, block until manual reset or next day.
    return {"active":True,"loss_streak":streak,"needed":needed,"hours":hours,"message":f"Cooldown active after {streak} consecutive losses."}

def no_trade_score(snapshot=None):
    if snapshot is None:
        snapshot=market()
    vals=[x for x in snapshot.values() if not x.get("error")]
    if not vals:
        return {"score":100,"reason":"No reliable market data. No trade."}
    strong=sum(1 for x in vals if str(x.get("signal","")).startswith("STRONG"))
    avg=sum(float(x.get("ai_score") or x.get("confidence") or 0) for x in vals)/len(vals)
    high_vol=sum(1 for x in vals if x.get("market_regime")=="HIGH_VOLATILITY")
    wait=sum(1 for x in vals if x.get("signal")=="WAIT")
    score=0
    if strong==0: score+=35
    if avg<60: score+=25
    if high_vol/len(vals)>0.35: score+=25
    if wait/len(vals)>0.65: score+=15
    score=max(0,min(100,round(score)))
    reasons=[]
    if strong==0: reasons.append("no strong setups")
    if avg<60: reasons.append("low average AI score")
    if high_vol/len(vals)>0.35: reasons.append("too much high volatility")
    if wait/len(vals)>0.65: reasons.append("most instruments are WAIT")
    return {"score":score,"strong_setups":strong,"avg_ai_score":round(avg,1),"high_volatility_count":high_vol,"wait_count":wait,"total":len(vals),"reason":"; ".join(reasons) or "Market quality acceptable."}

def daily_capital_status():
    s=get_settings()
    account=float(s.get("account_size",0) or 0)
    allowed=account*float(s.get("daily_capital_allocation_pct",20))/100
    used=0
    for t in trades_today():
        used += abs(float(t.get("entry") or 0)*float(t.get("qty") or 0))
    return {"account":account,"allowed_today":round(allowed,2),"used_today":round(used,2),"remaining_today":round(max(0,allowed-used),2),"used_pct":round(used/allowed*100,1) if allowed else 0}

def safety_status(snapshot=None):
    s=get_settings()
    nt=no_trade_score(snapshot)
    cd=cooldown_status()
    cap=daily_capital_status()
    blocked=[]
    if s.get("kill_switch_enabled"):
        blocked.append("Kill switch is ON")
    if cd["active"]:
        blocked.append(cd["message"])
    if nt["score"] >= float(s.get("min_no_trade_score",55)):
        blocked.append(f"No-trade score too high: {nt['score']}/100")
    if cap["remaining_today"] <= 0:
        blocked.append("Daily capital allocation exhausted")
    return {"allowed":len(blocked)==0,"blocked":blocked,"no_trade":nt,"cooldown":cd,"capital":cap,"kill_switch":s.get("kill_switch_enabled",False)}

