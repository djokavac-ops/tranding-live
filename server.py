def monte_carlo_simulation(trials=300):
    trades=closed_trades_for_symbol()
    pnls=[float(t.get("pnl") or 0) for t in trades]
    if len(pnls)<10:
        return {"ready":False,"message":"Need at least 10 closed trades for Monte Carlo.","trades":len(pnls)}
    import random
    finals=[]; maxdds=[]
    for _ in range(int(trials)):
        sample=[random.choice(pnls) for _ in range(min(100,len(pnls)*2))]
        run=0; peak=0; dd=0
        for p in sample:
            run+=p
            peak=max(peak,run)
            dd=max(dd,peak-run)
        finals.append(run); maxdds.append(dd)
    finals.sort(); maxdds.sort()
    def pct(arr,p): return round(arr[int(len(arr)*p)],2)
    return {"ready":True,"trials":trials,"p10_profit":pct(finals,0.10),"median_profit":pct(finals,0.50),"p90_profit":pct(finals,0.90),"median_drawdown":pct(maxdds,0.50),"p90_drawdown":pct(maxdds,0.90),"message":"Monte Carlo uses historical paper PnL distribution; not a guarantee."}

