def strategy_lab_summary():
    rows=q("SELECT * FROM ai_decision_audit ORDER BY id DESC LIMIT 1000", fetch=True)
    trades=all_trades()
    closed=[t for t in trades if t.get("status")!="OPEN"]

    strategy_stats={}
    for r in rows:
        st=r.get("strategy") or "unknown"
        strategy_stats.setdefault(st,{"signals":0,"avg_score":0,"buy_signals":0,"sell_signals":0,"wait_signals":0})
        d=strategy_stats[st]
        d["signals"]+=1
        d["avg_score"]+=float(r.get("final_score") or 0)
        dec=r.get("decision") or ""
        if "BUY" in dec: d["buy_signals"]+=1
        elif "SELL" in dec: d["sell_signals"]+=1
        else: d["wait_signals"]+=1

    for st,d in strategy_stats.items():
        d["avg_score"]=round(d["avg_score"]/d["signals"],2) if d["signals"] else 0
        d["paper_trades"]=0
        d["pnl"]=0
        d["wins"]=0
        d["losses"]=0
        d["win_rate"]=0
        d["profit_factor"]=0

    for t in closed:
        st=t.get("strategy") or "unknown"
        strategy_stats.setdefault(st,{"signals":0,"avg_score":0,"buy_signals":0,"sell_signals":0,"wait_signals":0,"paper_trades":0,"pnl":0,"wins":0,"losses":0,"win_rate":0,"profit_factor":0})
        d=strategy_stats[st]
        pnl=float(t.get("pnl") or 0)
        d["paper_trades"]+=1
        d["pnl"]+=pnl
        if pnl>0: d["wins"]+=1
        if pnl<0: d["losses"]+=1

    recommendations=[]
    for st,d in strategy_stats.items():
        wins_pnl=sum(float(t.get("pnl") or 0) for t in closed if (t.get("strategy") or "unknown")==st and float(t.get("pnl") or 0)>0)
        losses_pnl=abs(sum(float(t.get("pnl") or 0) for t in closed if (t.get("strategy") or "unknown")==st and float(t.get("pnl") or 0)<0))
        d["pnl"]=round(d.get("pnl",0),2)
        d["win_rate"]=round(d["wins"]/d["paper_trades"]*100,1) if d.get("paper_trades") else 0
        d["profit_factor"]=round(wins_pnl/losses_pnl,2) if losses_pnl else 0
        if d.get("paper_trades",0) >= 10:
            if d["profit_factor"] >= 1.5 and d["win_rate"] >= 50:
                recommendations.append({"strategy":st,"action":"keep_or_increase","reason":"Good paper performance so far"})
            elif d["profit_factor"] < 1:
                recommendations.append({"strategy":st,"action":"reduce","reason":"Weak paper performance so far"})
        elif d.get("signals",0)>0:
            recommendations.append({"strategy":st,"action":"observe","reason":"Not enough closed trades yet"})

    ranked=sorted(strategy_stats.items(), key=lambda x:(x[1].get("profit_factor",0),x[1].get("pnl",0),x[1].get("avg_score",0)), reverse=True)
    return {"strategies":dict(ranked),"recommendations":recommendations[:10]}

