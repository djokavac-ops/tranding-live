def market_comment(vals):
    total=len(vals)
    if not total: return "Nema dovoljno podataka za komentar tržišta."
    strong_buy=sum(1 for x in vals if x.get("signal")=="STRONG BUY")
    strong_sell=sum(1 for x in vals if x.get("signal")=="STRONG SELL")
    buy_watch=sum(1 for x in vals if "BUY" in x.get("signal",""))
    sell_watch=sum(1 for x in vals if "SELL" in x.get("signal",""))
    avg=sum(x.get("confidence",0) for x in vals)/total
    mood="bullish" if strong_buy>strong_sell and buy_watch>sell_watch else "bearish" if strong_sell>strong_buy and sell_watch>buy_watch else "mixed/neutral"
    return f"AI market scan: režim je {mood}. Analizirano {total} instrumenata. Strong BUY: {strong_buy}, Strong SELL: {strong_sell}, prosečan confidence {round(avg)}%."

