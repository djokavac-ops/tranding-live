def update_open(snapshot):
    for t in open_trades():
        x=snapshot.get(t["symbol"])
        if not x or x.get("error"): continue
        price=float(x["price"]); side=t["side"]
        pnl=(price-t["entry"])*t["qty"] if side=="BUY" else (t["entry"]-price)*t["qty"]
        status="OPEN"
        if side=="BUY" and price<=t["stop"]: status="STOP_LOSS"
        if side=="BUY" and price>=t["target1"]: status="TAKE_PROFIT"
        if side=="SELL" and price>=t["stop"]: status="STOP_LOSS"
        if side=="SELL" and price<=t["target1"]: status="TAKE_PROFIT"
        closed=now_iso() if status!="OPEN" else None
        q("UPDATE paper_trades SET current_price=:price,pnl=:pnl,status=:status,closed_at=:closed WHERE id=:id",
          {"price":price,"pnl":round(pnl,2),"status":status,"closed":closed,"id":t["id"]})
        if status!="OPEN": send_telegram(f"Paper close {status}: {t['side']} {t['name']} PnL {round(pnl,2)} EUR")

def best_candidate(snapshot,settings):
    open_symbols={t["symbol"] for t in open_trades()}
    candidates=[]
    for symbol,x in snapshot.items():
        if x.get("error") or symbol in open_symbols: continue
        if x["signal"] not in ("STRONG BUY","STRONG SELL"): continue
        if x["confidence"]<settings["min_confidence"] or x["risk_reward"]<settings["min_risk_reward"]: continue
        if x["risk"]=="HIGH" and x["confidence"]<90: continue
        candidates.append(x)
    candidates.sort(key=lambda x:(x["confidence"],x["risk_reward"],-abs(x["change_5d"])),reverse=True)
    return candidates[0] if candidates else None

def record_engine(status,opened,message):
    q("INSERT INTO engine_runs(ts,status,opened_trade,message) VALUES(:ts,:status,:opened,:message)",
      {"ts":now_iso(),"status":status,"opened":1 if opened else 0,"message":message})

def run_cycle():
    with ENGINE_LOCK:
        settings=get_settings()
        snap=market(force=True); update_open(snap)
        p=pnl_today(); todays=trades_today(); opened=open_trades()
        checks=[(p>=settings["daily_target"],"STOP: daily target reached"),
                (p<=-settings["daily_max_loss"],"STOP: daily max loss reached"),
                (len(todays)>=int(settings["max_trades_per_day"]),"STOP: max trades reached"),
                (len(opened)>=int(settings["max_open_trades"]),"STOP: max open trades reached")]
        for cond,msg in checks:
            if cond:
                record_engine("STOP",False,msg)
                return {"executed":False,"status":msg,"daily_pnl":p}
        cand=best_candidate(snap,settings)
        if not cand:
            msg="No qualified professional setup"
            record_engine("WAIT",False,msg)
            return {"executed":False,"status":msg,"daily_pnl":p}
        risk_money=settings["account_size"]*settings["risk_per_trade_pct"]/100
        stop_dist=abs(cand["entry"]-cand["stop"])
        qty=risk_money/stop_dist if stop_dist>0 else 0
        q("""INSERT INTO paper_trades(day,opened_at,closed_at,symbol,name,group_name,side,entry,current_price,stop,target1,target2,qty,confidence,risk_reward,status,pnl,reason,ai_explanation)
             VALUES(:day,:opened_at,:closed_at,:symbol,:name,:group_name,:side,:entry,:current_price,:stop,:target1,:target2,:qty,:confidence,:risk_reward,:status,:pnl,:reason,:ai)""",
          {"day":today(),"opened_at":now_iso(),"closed_at":None,"symbol":cand["symbol"],"name":cand["name"],"group_name":cand["group"],
           "side":cand["side"],"entry":cand["entry"],"current_price":cand["price"],"stop":cand["stop"],"target1":cand["target1"],"target2":cand["target2"],
           "qty":qty,"confidence":cand["confidence"],"risk_reward":cand["risk_reward"],"status":"OPEN","pnl":0,"reason":cand["reason"],"ai":cand["ai_explanation"]})
        msg=f"Paper open: {cand['side']} {cand['name']} @ {cand['entry']} | SL {cand['stop']} | TP1 {cand['target1']} | conf {cand['confidence']}%"
        send_telegram(msg); record_engine("OPENED",True,msg)
        return {"executed":True,"status":msg,"candidate":cand,"daily_pnl":pnl_today()}

def report_for_trades(trades,title):
    total=len(trades); closed=[t for t in trades if t["status"]!="OPEN"]
    wins=[t for t in closed if float(t["pnl"])>0]; losses=[t for t in closed if float(t["pnl"])<0]
    pnl=round(sum(float(t["pnl"]) for t in trades),2)
    gross_win=sum(float(t["pnl"]) for t in wins); gross_loss=abs(sum(float(t["pnl"]) for t in losses))
    winrate=round(len(wins)/len(closed)*100,1) if closed else 0
    profit_factor=round(gross_win/gross_loss,2) if gross_loss>0 else (round(gross_win,2) if gross_win>0 else 0)
    best=max(trades,key=lambda x:float(x["pnl"]),default=None); worst=min(trades,key=lambda x:float(x["pnl"]),default=None)
    by={}
    for t in trades: by[t["symbol"]]=by.get(t["symbol"],0)+float(t["pnl"])
    top=max(by.items(),key=lambda x:x[1],default=None)
    return {"title":title,"total_trades":total,"closed_trades":len(closed),"open_trades":total-len(closed),"wins":len(wins),"losses":len(losses),
            "win_rate":winrate,"profit_factor":profit_factor,"pnl":pnl,
            "best_trade":dict(best) if best else None,"worst_trade":dict(worst) if worst else None,
            "top_symbol":{"symbol":top[0],"pnl":round(top[1],2)} if top else None,
            "comment":"Paper rezultat je pozitivan." if pnl>0 else "Paper rezultat je negativan ili neutralan; smanjiti rizik i pratiti kvalitet signala."}


def state_get(key, default=None):
    rows=q("SELECT value FROM automation_state WHERE key=:key", {"key":key}, fetch=True)
    if not rows:
        return default
    try:
        return json.loads(rows[0]["value"])
    except Exception:
        return rows[0]["value"]

def state_set(key, value):
    q("DELETE FROM automation_state WHERE key=:key", {"key":key})
    q("INSERT INTO automation_state(key,value) VALUES(:key,:value)", {"key":key,"value":json.dumps(value)})

def compact_report_text(report):
    return (
        f"{report['title']}\n"
        f"PnL: {report['pnl']} EUR\n"
        f"Trejdova: {report['total_trades']} | Open: {report['open_trades']}\n"
        f"Win rate: {report['win_rate']}% | Profit factor: {report['profit_factor']}\n"
        f"Top: {report['top_symbol']['symbol'] + ' ' + str(report['top_symbol']['pnl']) + ' EUR' if report.get('top_symbol') else '—'}\n"
        f"{report['comment']}"
    )

def maybe_send_scheduled_reports():
    settings=get_settings()
    now=datetime.now(timezone.utc)
    hour=int(settings.get("report_hour_utc",21))
    if now.hour < hour:
        return
    day_key=today()
    if settings.get("telegram_daily_report"):
        last=state_get("last_daily_report")
        if last != day_key:
            send_telegram("📊 Trading AI Daily Report\n\n" + compact_report_text(report_for_trades(trades_today(),"Daily Report")))
            state_set("last_daily_report", day_key)
            record_engine("REPORT", False, "Daily Telegram report processed")
    if settings.get("telegram_weekly_report") and now.weekday()==6:
        week_key=f"{now.isocalendar().year}-W{now.isocalendar().week}"
        last=state_get("last_weekly_report")
        if last != week_key:
            send_telegram("📈 Trading AI Weekly Report\n\n" + compact_report_text(report_for_trades(trades_since(7),"Weekly Report")))
            state_set("last_weekly_report", week_key)
            record_engine("REPORT", False, "Weekly Telegram report processed")

def engine_loop():
    while True:
        try:
            settings=get_settings()
            if settings.get("auto_engine_enabled"):
                run_cycle()
            maybe_send_scheduled_reports()
            time.sleep(max(60,int(settings.get("engine_interval_seconds",300))))
        except Exception as e:
            record_engine("ERROR",False,str(e))
            time.sleep(120)

threading.Thread(target=engine_loop,daemon=True).start()

