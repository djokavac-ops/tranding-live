def intelligence_memory_build():
    trades=closed_trades_for_symbol()
    insights=[]
    by_strategy={}
    by_symbol={}
    for t in trades:
        pnl=float(t.get("pnl") or 0)
        st=t.get("strategy") or "unknown"
        sy=t.get("symbol") or "unknown"
        for bucket,key in ((by_strategy,st),(by_symbol,sy)):
            bucket.setdefault(key,{"trades":0,"wins":0,"pnl":0,"gross_win":0,"gross_loss":0})
            d=bucket[key]
            d["trades"]+=1
            d["pnl"]+=pnl
            if pnl>0:
                d["wins"]+=1
                d["gross_win"]+=pnl
            if pnl<0:
                d["gross_loss"]+=abs(pnl)

    def save_memory(mtype, subject, insight, confidence, evidence):
        try:
            q("""INSERT INTO ai_memory(ts,memory_type,subject,insight,confidence,evidence_count)
            VALUES(:ts,:mtype,:subject,:insight,:confidence,:evidence)""",
            {"ts":now_iso(),"mtype":mtype,"subject":subject,"insight":insight,"confidence":confidence,"evidence":evidence})
        except Exception:
            pass
        insights.append({"type":mtype,"subject":subject,"insight":insight,"confidence":confidence,"evidence":evidence})

    for st,d in by_strategy.items():
        if d["trades"]>=5:
            wr=d["wins"]/d["trades"]*100
            pf=round(d["gross_win"]/d["gross_loss"],2) if d["gross_loss"] else 0
            if pf>=1.5 and wr>=50:
                save_memory("strategy",st,f"{st} currently shows positive paper edge: win rate {round(wr,1)}%, profit factor {pf}.",min(90,d["trades"]*5),d["trades"])
            elif pf<1:
                save_memory("strategy",st,f"{st} is underperforming in paper results: win rate {round(wr,1)}%, profit factor {pf}. Reduce usage.",min(90,d["trades"]*5),d["trades"])
    for sy,d in by_symbol.items():
        if d["trades"]>=5:
            wr=d["wins"]/d["trades"]*100
            pf=round(d["gross_win"]/d["gross_loss"],2) if d["gross_loss"] else 0
            save_memory("symbol",sy,f"{sy} paper profile: win rate {round(wr,1)}%, profit factor {pf}, total PnL {round(d['pnl'],2)}.",min(90,d["trades"]*5),d["trades"])
    if not insights:
        save_memory("system","global","Not enough closed paper trades for reliable memory. Continue paper testing.",20,len(trades))
    return {"built":len(insights),"insights":insights}

