def fetch_chart(ticker,range_="1y",interval="1d"):
    r=requests.get(f"https://query1.finance.yahoo.com/v8/finance/chart/{ticker}",params={"range":range_,"interval":interval},headers={"User-Agent":"Mozilla/5.0"},timeout=15)
    r.raise_for_status()
    result=r.json().get("chart",{}).get("result")
    if not result: raise ValueError("No market data")
    data=result[0]; quote=data["indicators"]["quote"][0]; rows=[]
    for i,ts in enumerate(data["timestamp"]):
        o,h,l,c=quote["open"][i],quote["high"][i],quote["low"][i],quote["close"][i]
        v=quote.get("volume",[0]*len(data["timestamp"]))[i]
        if None in (o,h,l,c): continue
        rows.append({"date":datetime.fromtimestamp(ts,timezone.utc).strftime("%Y-%m-%d"),"open":float(o),"high":float(h),"low":float(l),"close":float(c),"volume":float(v or 0)})
    if len(rows)<90: raise ValueError("Not enough history")
    return rows

def ema(vals,span):
    a=2/(span+1); out=vals[0]
    for v in vals[1:]: out=a*v+(1-a)*out
    return out
def sma(vals,n): 
    vals=vals[-n:] if len(vals)>=n else vals
    return sum(vals)/len(vals) if vals else 0
def rsi(vals,n=14):
    gains=[]; losses=[]
    for i in range(-n,0):
        ch=vals[i]-vals[i-1]; gains.append(max(ch,0)); losses.append(abs(min(ch,0)))
    ag=sum(gains)/n; al=sum(losses)/n
    return 100 if al==0 else 100-100/(1+ag/al)
def atr(rows,n=14):
    trs=[]; prev=rows[-n-1]["close"]
    for row in rows[-n:]:
        trs.append(max(row["high"]-row["low"],abs(row["high"]-prev),abs(row["low"]-prev))); prev=row["close"]
    return sum(trs)/len(trs)
def macd(vals): return ema(vals[-120:],12)-ema(vals[-140:],26)
def pct(vals,n): return 0 if len(vals)<=n else (vals[-1]/vals[-n-1]-1)*100
def bollinger(vals,n=20):
    recent=vals[-n:]; mean=sum(recent)/len(recent); sd=math.sqrt(sum((x-mean)**2 for x in recent)/len(recent))
    return 0.5 if sd==0 else (vals[-1]-(mean-2*sd))/(4*sd)
def clamp(x): return max(0,min(100,x))

def regime(rows):
    closes=[r["close"] for r in rows]; price=closes[-1]; e50=ema(closes[-160:],50); e200=ema(closes,200); av=atr(rows)
    if av/price>0.055: return "HIGH_VOLATILITY"
    if e50>e200 and pct(closes,20)>0: return "BULL"
    if e50<e200 and pct(closes,20)<0: return "BEAR"
    return "SIDEWAYS"

def analyze(rows,meta):
    closes=[r["close"] for r in rows]; vols=[r["volume"] for r in rows]; price=closes[-1]
    e8,e20,e50,e100,e200=ema(closes[-60:],8),ema(closes[-80:],20),ema(closes[-160:],50),ema(closes[-220:],100),ema(closes,200)
    rv,av,mv,bb=rsi(closes),atr(rows),macd(closes),bollinger(closes)
    ch5,ch20,ch60=pct(closes,5),pct(closes,20),pct(closes,60)
    volavg=sma(vols,20); volfactor=vols[-1]/volavg if volavg else 1
    high20=max(r["high"] for r in rows[-20:]); low20=min(r["low"] for r in rows[-20:])
    rr=2.2/1.5
    reg=regime(rows)

    trend=50+(10 if price>e20 else -10)+(20 if e8>e20>e50 else -20 if e8<e20<e50 else 0)+(20 if e50>e200 else -20)+(5 if e100>e200 else -5)
    momentum=50+(15 if mv>0 else -15)+(10 if ch5>0 else -10)+(15 if ch20>0 else -15)+(10 if ch60>0 else -10)+(10 if 48<=rv<=66 else -12 if rv>75 or rv<35 else 0)
    volume=50+(25 if volfactor>1.6 else 15 if volfactor>1.25 else -15 if volfactor<0.75 else 0)
    volratio=av/price if price else 0
    volatility=25 if volratio>0.08 else 40 if volratio>0.055 else 60 if volratio>0.035 else 80
    rr_score=75
    risk_score=clamp(100-(100-volatility))
    scores={"trend_score":round(clamp(trend),1),"momentum_score":round(clamp(momentum),1),"volume_score":round(clamp(volume),1),"volatility_score":round(clamp(volatility),1),"risk_score":round(risk_score,1),"rr_score":rr_score}
    final=round(clamp(scores["trend_score"]*.28+scores["momentum_score"]*.24+scores["volume_score"]*.14+scores["volatility_score"]*.14+scores["rr_score"]*.14+scores["risk_score"]*.06-(8 if reg=="HIGH_VOLATILITY" else 3 if reg=="SIDEWAYS" else 0)),1)

    bull=(price>e20)+(e8>e20>e50)+(e50>e200)+(mv>0)+(ch5>0 and ch20>0)+(price>high20*.995)
    bear=(price<e20)+(e8<e20<e50)+(e50<e200)+(mv<0)+(ch5<0 and ch20<0)+(price<low20*1.005)
    side="BUY" if bull>bear+1 else "SELL" if bear>bull+1 else "WAIT"
    strategy="trend_following" if ((reg=="BULL" and side=="BUY") or (reg=="BEAR" and side=="SELL")) else "momentum_breakout" if scores["momentum_score"]>=75 and scores["volume_score"]>=65 else "risk_reduction" if reg=="HIGH_VOLATILITY" else "balanced_swing"
    rejection=[]
    if reg=="HIGH_VOLATILITY" and final<92: rejection.append("high volatility")
    if scores["risk_score"]<45: rejection.append("weak risk score")
    if side=="BUY" and reg=="BEAR" and final<92: rejection.append("BUY conflicts with BEAR regime")
    if side=="SELL" and reg=="BULL" and final<92: rejection.append("SELL conflicts with BULL regime")
    if side=="WAIT": rejection.append("time/structure not aligned")

    signal="WAIT"
    if not rejection and side=="BUY" and final>=86: signal="STRONG BUY"
    elif not rejection and side=="BUY" and final>=72: signal="BUY WATCH"
    elif not rejection and side=="SELL" and final>=86: signal="STRONG SELL"
    elif not rejection and side=="SELL" and final>=72: signal="SELL WATCH"

    stop_dist=max(av*1.5,price*.01); t1_dist=max(av*2.2,price*.014); t2_dist=max(av*3.4,price*.022)
    stop,t1,t2=(price+stop_dist,price-t1_dist,price-t2_dist) if side=="SELL" else (price-stop_dist,price+t1_dist,price+t2_dist)
    real_rr=abs(t1-price)/abs(price-stop) if abs(price-stop)>0 else 0
    reason=f"regime={reg}; strategy={strategy}; trend={scores['trend_score']}; momentum={scores['momentum_score']}; volume={scores['volume_score']}; risk={scores['risk_score']}; rr={round(real_rr,2)}"
    ai=f"{meta['name']}: {'WAIT - odbijeno: '+', '.join(rejection) if rejection else signal}. AI score {final}/100. {reason}"
    return {"symbol":None,"name":meta["name"],"ticker":meta["ticker"],"group":meta["group"],"price":round(price,4),"signal":signal,"side":side,"confidence":int(round(final)),"ai_score":final,**scores,"market_regime":reg,"strategy":strategy,"risk":"LOW" if volratio<.025 else "MEDIUM" if volratio<.055 else "HIGH","trend":"BULLISH" if e50>e200 else "BEARISH","rsi":round(rv,2),"atr":round(av,4),"macd":round(mv,4),"bollinger_pos":round(bb,2),"candle":"N/A","ema8":round(e8,4),"ema20":round(e20,4),"ema50":round(e50,4),"ema100":round(e100,4),"ema200":round(e200,4),"change_5d":round(ch5,2),"change_20d":round(ch20,2),"change_60d":round(ch60,2),"entry":round(price,4),"stop":round(stop,4),"target1":round(t1,4),"target2":round(t2,4),"risk_reward":round(real_rr,2),"reason":reason,"ai_explanation":ai,"updated_at":now_iso()}

