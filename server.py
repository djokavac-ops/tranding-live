def broker_preflight(order, broker_name=None):
    s=get_settings()
    broker=get_broker(broker_name)
    errors=[]
    warnings=[]
    v=broker.validate_order(order)
    if not v["ok"]:
        errors.extend(v["errors"])
    notional=abs(float(order.get("entry",0))*float(order.get("qty",0)))
    if broker.name!="paper":
        if broker.name=="ibkr" and s.get("ibkr_paper_enabled",True):
            # IBKR paper mode is allowed only through approval/manual execute path.
            pass
        else:
            if not s.get("broker_live_enabled",False):
                errors.append("Live broker execution locked")
        if s.get("broker_confirm_required",True) and broker.name!="ibkr":
            errors.append("Manual confirmation required")
        if notional>float(s.get("max_live_order_value",100)) and broker.name!="ibkr":
            errors.append(f"Order value {round(notional,2)} exceeds max_live_order_value")
    safe=safety_status()
    if not safe["allowed"]:
        errors.extend(["Safety block: "+x for x in safe["blocked"]])
    if float(order.get("ai_score",0))<float(s.get("min_confidence",82)):
        warnings.append("AI score below configured min_confidence")
    return {"ok":len(errors)==0,"broker":broker.status(),"order_value":round(notional,2),"errors":errors,"warnings":warnings,"safety":safe}

def execute_approval_with_broker(approval_id, broker_name=None):
    rows=q("SELECT * FROM trade_approvals WHERE id=:id", {"id":approval_id}, fetch=True)
    if not rows:
        return {"ok":False,"error":"approval not found"}
    a=rows[0]
    if a["status"]!="PENDING":
        return {"ok":False,"error":"approval already decided"}
    order=order_from_approval(a)
    broker=get_broker(broker_name or a.get("broker") or "paper")
    pre=broker_preflight(order, broker.name)
    if not pre["ok"] and broker.name!="paper":
        q("UPDATE trade_approvals SET status='BLOCKED', decided_at=:ts WHERE id=:id", {"ts":now_iso(),"id":approval_id})
        q("""INSERT INTO broker_orders(created_at,approval_id,broker,mode,symbol,side,qty,order_type,status,response)
        VALUES(:ts,:aid,:broker,'preflight',:symbol,:side,:qty,'market','BLOCKED',:response)""",
        {"ts":now_iso(),"aid":approval_id,"broker":broker.name,"symbol":order["symbol"],"side":order["side"],"qty":order["qty"],"response":str(pre)})
        return {"ok":False,"status":"blocked","preflight":pre}
    if broker.name=="paper":
        return execute_paper_from_approval(a)
    result=broker.place_order(order)
    mode="ibkr_paper" if broker.name=="ibkr" and get_settings().get("ibkr_paper_enabled",True) else "live_locked"
    q("""INSERT INTO broker_orders(created_at,approval_id,broker,mode,symbol,side,qty,order_type,status,response)
    VALUES(:ts,:aid,:broker,:mode,:symbol,:side,:qty,'market',:status,:response)""",
    {"ts":now_iso(),"aid":approval_id,"broker":broker.name,"mode":mode,"symbol":order["symbol"],"side":order["side"],"qty":order["qty"],"status":result.get("status","UNKNOWN"),"response":str(result)})
    if result.get("ok"):
        q("UPDATE trade_approvals SET status='APPROVED_SENT_TO_BROKER', decided_at=:ts WHERE id=:id", {"ts":now_iso(),"id":approval_id})
    return result

