        if not x or x.get("error"): continue
        price=float(x["price"]); side=t["side"]; pnl=(price-t["entry"])*t["qty"] if side=="BUY" else (t["entry"]-price)*t["qty"]; status="OPEN"
        if side=="BUY" and price<=t["stop"]: status="STOP_LOSS"
        if side=="BUY" and price>=t["target1"]: status="TAKE_PROFIT"
        if side=="SELL" and price>=t["stop"]: status="STOP_LOSS"
        if side=="SELL" and price<=t["target1"]: status="TAKE_PROFIT"
        q("UPDATE paper_trades SET current_price=:p,pnl=:pnl,status=:s,closed_at=:c WHERE id=:id",{"p":price,"pnl":round(pnl,2),"s":status,"c":now_iso() if status!="OPEN" else None,"id":t["id"]})

