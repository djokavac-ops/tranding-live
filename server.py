def risk_guardrails(candidate=None):
    s=get_settings()
    health=portfolio_health()
    account=float(s.get("account_size",0) or 0)
    blocked=[]
    warnings=[]
    if health["total_risk_pct"] >= float(s.get("max_portfolio_risk_pct",3)):
        blocked.append("Portfolio risk limit reached")
    for group,exposure in health.get("exposure_by_group",{}).items():
        pct=exposure/account*100 if account else 0
        if pct>=float(s.get("max_group_exposure_pct",45)):
            blocked.append(f"Group exposure too high: {group} {round(pct,1)}%")
        elif pct>=float(s.get("max_group_exposure_pct",45))*0.8:
            warnings.append(f"Group exposure near limit: {group} {round(pct,1)}%")
    for symbol,exposure in health.get("exposure_by_symbol",{}).items():
        pct=exposure/account*100 if account else 0
        if pct>=float(s.get("max_symbol_exposure_pct",25)):
            blocked.append(f"Symbol exposure too high: {symbol} {round(pct,1)}%")
    if candidate:
        if candidate.get("market_regime")=="HIGH_VOLATILITY" and candidate.get("ai_score",0)<92:
            blocked.append("Candidate blocked: high volatility requires AI score >= 92")
        if candidate.get("risk_score",100)<45:
            blocked.append("Candidate blocked: weak risk score")
    return {"blocked":blocked,"warnings":warnings,"health":health,"allowed":len(blocked)==0}

def simple_strategy_signal_from_rows(rows, strategy="trend_following"):
    closes=[r["close"] for r in rows]
    if len(closes)<220: return "WAIT"
    price=closes[-1]
    e20=ema(closes[-80:],20)
    e50=ema(closes[-160:],50)
    e200=ema(closes,200)
    mv=macd(closes)
    rv=rsi(closes)
    if strategy=="trend_following":
        if price>e20 and e50>e200 and mv>0 and 45<=rv<=72: return "BUY"
        if price<e20 and e50<e200 and mv<0 and 28<=rv<=55: return "SELL"
    elif strategy=="momentum_breakout":
        high20=max(r["high"] for r in rows[-20:])
        low20=min(r["low"] for r in rows[-20:])
        if price>=high20*0.998 and mv>0 and rv<75: return "BUY"
        if price<=low20*1.002 and mv<0 and rv>25: return "SELL"
    elif strategy=="balanced_swing":
        if price>e20 and e20>e50 and 48<=rv<=68: return "BUY"
        if price<e20 and e20<e50 and 32<=rv<=52: return "SELL"
    elif strategy=="mean_reversion_filter":
        bb=bollinger(closes)
        if bb<0.08 and rv<35: return "BUY"
        if bb>0.92 and rv>68: return "SELL"
    return "WAIT"

def backtest_symbol(symbol, strategy="trend_following", lookback=260):
    symbols=load_symbols()
    symbol=symbol.upper()
    if symbol not in symbols:
        return {"error":"Unknown symbol"}
    rows=fetch_chart(symbols[symbol]["ticker"], range_="2y", interval="1d")
    if len(rows)<120:
        return {"error":"Not enough history"}
    trades=[]
    position=None
    start=max(80, len(rows)-int(lookback))
    for i in range(start, len(rows)-1):
        hist=rows[:i]
        sig=simple_strategy_signal_from_rows(hist, strategy)
        price=rows[i]["close"]
        av=atr(hist)
        if position:
            next_price=rows[i]["close"]
            side=position["side"]
            pnl=(next_price-position["entry"]) if side=="BUY" else (position["entry"]-next_price)
            hit_stop=(side=="BUY" and next_price<=position["stop"]) or (side=="SELL" and next_price>=position["stop"])
            hit_target=(side=="BUY" and next_price>=position["target"]) or (side=="SELL" and next_price<=position["target"])
            max_hold=(i-position["i"])>=12
            if hit_stop or hit_target or max_hold:
                trades.append({"entry":position["entry"],"exit":next_price,"side":side,"pnl":round(pnl,4),"reason":"target" if hit_target else "stop" if hit_stop else "max_hold"})
                position=None
        if not position and sig in ("BUY","SELL"):
            stop_dist=max(av*1.5, price*0.01)
            target_dist=max(av*2.2, price*0.014)
            position={"side":sig,"entry":price,"i":i,"stop":price-stop_dist if sig=="BUY" else price+stop_dist,"target":price+target_dist if sig=="BUY" else price-target_dist}
    wins=[t for t in trades if t["pnl"]>0]
    losses=[t for t in trades if t["pnl"]<0]
    gross_win=sum(t["pnl"] for t in wins)
    gross_loss=abs(sum(t["pnl"] for t in losses))
    equity=[]; run=0; peak=0; dd=0
    for t in trades:
        run+=t["pnl"]; peak=max(peak,run); dd=max(dd,peak-run); equity.append(round(run,4))
    return {
        "symbol":symbol,
        "name":symbols[symbol]["name"],
        "strategy":strategy,
        "trades":len(trades),
        "wins":len(wins),
        "losses":len(losses),
        "win_rate":round(len(wins)/len(trades)*100,1) if trades else 0,
        "profit_factor":round(gross_win/gross_loss,2) if gross_loss else 0,
        "total_points":round(sum(t["pnl"] for t in trades),4),
        "max_drawdown_points":round(dd,4),
        "equity_curve":equity[-100:],
        "recent_trades":trades[-20:],
        "warning":"Backtest is simplified and not a guarantee of future results."
    }

def run_watchlist_backtest(strategy="trend_following"):
    out=[]
    for symbol in list(load_symbols().keys())[:25]:
        try:
            r=backtest_symbol(symbol,strategy=strategy,lookback=260)
            if not r.get("error"): out.append(r)
        except Exception as e:
            pass
    out.sort(key=lambda x:(x["profit_factor"],x["win_rate"],x["trades"]), reverse=True)
    return {"strategy":strategy,"results":out[:15]}


