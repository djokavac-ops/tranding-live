def ai_capital_manager():
    snap=market()
    vals=[x for x in snap.values() if not x.get("error")]
    groups={}
    for x in vals:
        group=x.get("group","unknown")
        score=float(x.get("ai_score") or 0)
        if x.get("signal")=="WAIT":
            score*=0.25
        groups.setdefault(group,0)
        groups[group]+=score
    total=sum(groups.values()) or 1
    allocation={}
    for group,score in groups.items():
        pct=round(min(35, max(0, score/total*70)),1)
        allocation[group]=pct
    cash=round(100-sum(allocation.values()),1)
    if cash<10:
        # Keep some cash by scaling down.
        scale=90/sum(allocation.values()) if sum(allocation.values()) else 1
        allocation={k:round(v*scale,1) for k,v in allocation.items()}
        cash=round(100-sum(allocation.values()),1)
    return {"allocation_pct":allocation,"cash_pct":cash,"message":"Capital manager keeps cash reserve and allocates by current AI opportunity quality."}

