def multi_model_consensus(symbol):
    symbol=symbol.upper()
    snap=market()
    x=snap.get(symbol)
    if not x or x.get("error"):
        return {"error":"symbol not available"}
    committee=investment_committee_for_candidate(x)
    macro=macro_regime_plus()
    votes=[
        {"model":"TechnicalModel","score":x.get("ai_score",0),"vote":"APPROVE" if x.get("ai_score",0)>=75 else "WAIT"},
        {"model":"RiskModel","score":x.get("risk_score",50),"vote":"APPROVE" if x.get("risk_score",50)>=65 else "WAIT"},
        {"model":"CommitteeModel","score":committee.get("yes",0)*20,"vote":committee.get("decision","NO_CONSENSUS")},
        {"model":"MacroModel","score":macro.get("confidence",50),"vote":"APPROVE" if macro.get("regime")!="RISK_OFF_HIGH_VOL" else "WAIT"},
    ]
    avg=sum(float(v.get("score") or 0) for v in votes)/len(votes)
    approve=sum(1 for v in votes if v["vote"]=="APPROVE")
    decision="APPROVE" if approve>=3 and avg>=70 else "WAIT"
