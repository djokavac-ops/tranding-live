def hybrid_decision_for_candidate(candidate):
    s=get_settings()
    score=float(candidate.get("ai_score") or candidate.get("confidence") or 0)
    auto_th=float(s.get("hybrid_auto_threshold",96))
    approval_th=float(s.get("hybrid_approval_threshold",82))
    idea_th=float(s.get("hybrid_idea_threshold",65))
    safe=safety_status()
    committee=investment_committee_for_candidate(candidate)
    if not s.get("hybrid_mode_enabled",True):
        return {"mode":"disabled","action":"use_existing_execution_mode","reason":"Hybrid mode is off."}
    if not safe.get("allowed"):
        return {"mode":"blocked","action":"no_trade","reason":"Safety blocked: "+"; ".join(safe.get("blocked",[]))}
    if committee.get("decision")!="APPROVE":
        return {"mode":"idea","action":"show_only","reason":"Investment Committee did not approve.", "committee":committee}
    if score>=auto_th and candidate.get("signal") in ("STRONG BUY","STRONG SELL"):
        return {"mode":"auto_candidate","action":"auto_allowed_if_paper_or_unlocked","reason":f"AI score {score} >= auto threshold {auto_th}.", "committee":committee}
    if score>=approval_th:
        return {"mode":"approval","action":"ask_user_approval","reason":f"AI score {score} >= approval threshold {approval_th}.", "committee":committee}
    if score>=idea_th:
        return {"mode":"idea","action":"show_only","reason":f"AI score {score} >= idea threshold {idea_th}, but not strong enough for approval.", "committee":committee}
    return {"mode":"ignore","action":"no_trade","reason":f"AI score {score} below idea threshold {idea_th}.", "committee":committee}

