def fetch_chart(ticker, range_="1y", interval="1d"):
    r = requests.get(f"https://query1.finance.yahoo.com/v8/finance/chart/{ticker}",
                     params={"range": range_, "interval": interval},
                     headers={"User-Agent": "Mozilla/5.0"}, timeout=15)
    r.raise_for_status()
    result = r.json().get("chart",{}).get("result")
    if not result: raise ValueError("No market data")
    data=result[0]; quote=data["indicators"]["quote"][0]; rows=[]
    for i,ts in enumerate(data["timestamp"]):
        o,h,l,c = quote["open"][i], quote["high"][i], quote["low"][i], quote["close"][i]
        v = quote.get("volume",[0]*len(data["timestamp"]))[i]
        if None in (o,h,l,c): continue
        rows.append({"date": datetime.fromtimestamp(ts,timezone.utc).strftime("%Y-%m-%d"),
                     "open":float(o),"high":float(h),"low":float(l),"close":float(c),"volume":float(v or 0)})
    if len(rows) < 90: raise ValueError("Not enough history")
    return rows

def ema(vals,span):
    alpha=2/(span+1); out=vals[0]
    for v in vals[1:]: out=alpha*v+(1-alpha)*out
    return out

def sma(vals,n):
    vals=vals[-n:] if len(vals)>=n else vals
    return sum(vals)/len(vals) if vals else 0

def rsi(vals,n=14):
    gains=[]; losses=[]
    for i in range(-n,0):
        ch=vals[i]-vals[i-1]; gains.append(max(ch,0)); losses.append(abs(min(ch,0)))
    ag=sum(gains)/n; al=sum(losses)/n
    return 100 if al==0 else 100-100/(1+ag/al)

def atr(rows,n=14):
    trs=[]; prev=rows[-n-1]["close"]
    for row in rows[-n:]:
        trs.append(max(row["high"]-row["low"], abs(row["high"]-prev), abs(row["low"]-prev)))
        prev=row["close"]
    return sum(trs)/len(trs)

def macd(vals): return ema(vals[-120:],12)-ema(vals[-140:],26)

def bollinger_pos(vals,n=20):
    recent=vals[-n:]; mean=sum(recent)/len(recent)
    sd=math.sqrt(sum((x-mean)**2 for x in recent)/len(recent))
    upper=mean+2*sd; lower=mean-2*sd
    return 0.5 if upper==lower else (vals[-1]-lower)/(upper-lower)

def pct(vals,n): return 0 if len(vals)<=n else (vals[-1]/vals[-n-1]-1)*100

def candle(rows):
    last=rows[-1]; prev=rows[-2]
    body=abs(last["close"]-last["open"]); rng=max(0.0001,last["high"]-last["low"])
    upper=last["high"]-max(last["close"],last["open"])
    lower=min(last["close"],last["open"])-last["low"]
    if body/rng < 0.12: return "DOJI",0
    if lower > body*2 and upper < body: return "HAMMER",4
    if upper > body*2 and lower < body: return "SHOOTING_STAR",-4
    if last["close"]>last["open"] and prev["close"]<prev["open"] and last["close"]>prev["open"] and last["open"]<prev["close"]:
        return "BULLISH_ENGULFING",6
    if last["close"]<last["open"] and prev["close"]>prev["open"] and last["open"]>prev["close"] and last["close"]<prev["open"]:
        return "BEARISH_ENGULFING",-6
    return "NONE",0

def normalize_score(raw):
    if raw >= 92: return min(96, 88 + round((raw-92)*0.8))
    if raw <= 8: return max(4, 12 - round((8-raw)*0.8))
    return raw

def explain(name, side, signal, confidence, risk, rr, reason):
    if side=="WAIT":
        return f"{name}: WAIT. Sistem ne vidi dovoljno čist setup; potvrde se ne poklapaju dovoljno dobro."
    action = "kupovinu" if side=="BUY" else "prodaju"
    return f"{name}: sistem bira {action} jer ima {signal} setup, confidence {confidence}%, rizik {risk}, RR oko 1:{rr}. Razlozi: {reason}."

def analyze(rows, meta):
    closes=[x["close"] for x in rows]; vols=[x["volume"] for x in rows]; price=closes[-1]
    e8=ema(closes[-60:],8); e20=ema(closes[-80:],20); e50=ema(closes[-160:],50); e100=ema(closes[-220:],100); e200=ema(closes,200)
    rv=rsi(closes); av=atr(rows); mv=macd(closes); bb=bollinger_pos(closes)
    ch5=pct(closes,5); ch20=pct(closes,20); ch60=pct(closes,60)
    volavg=sma(vols,20); volfactor=vols[-1]/volavg if volavg>0 else 1
    high20=max(x["high"] for x in rows[-20:]); low20=min(x["low"] for x in rows[-20:])
    high60=max(x["high"] for x in rows[-60:]); low60=min(x["low"] for x in rows[-60:])
    pat,pscore=candle(rows)
    raw=50; bull=[]; bear=[]
    if price>e20: raw+=6; bull.append("price above EMA20")
    else: raw-=6; bear.append("price below EMA20")
    if e8>e20>e50: raw+=10; bull.append("bullish EMA stack")
    elif e8<e20<e50: raw-=10; bear.append("bearish EMA stack")
    if e50>e200: raw+=12; bull.append("primary trend bullish")
    else: raw-=12; bear.append("primary trend bearish")
    raw += 4 if e100>e200 else -4
    if mv>0: raw+=6; bull.append("MACD positive")
    else: raw-=6; bear.append("MACD negative")
    if 50<=rv<=66: raw+=8; bull.append("RSI healthy")
    elif 66<rv<=72: raw+=2; bull.append("RSI strong")
    elif rv>75: raw-=9; bear.append("RSI overbought")
    elif rv<35: raw-=9; bear.append("RSI weak/oversold")
    elif rv<45: raw-=4
    if ch5>0 and ch20>0: raw+=8; bull.append("5d and 20d momentum positive")
    elif ch5<0 and ch20<0: raw-=8; bear.append("5d and 20d momentum negative")
    raw += 5 if ch60>0 else -5
    if price>high20*0.995: raw+=4; bull.append("near 20d breakout")
    if price<low20*1.005: raw-=4; bear.append("near 20d breakdown")
    if price>high60*0.995: raw+=3; bull.append("near 60d high")
    if price<low60*1.005: raw-=3; bear.append("near 60d low")
    if volfactor>1.4: raw+=3; bull.append("volume expansion")
    if bb>0.95: raw-=4; bear.append("Bollinger high")
    if bb<0.05: raw-=2; bear.append("Bollinger low")
    raw += pscore
    if pat!="NONE": (bull if pscore>=0 else bear).append(pat)
    score=normalize_score(max(0,min(100,round(raw))))
    if score>=86: signal,side="STRONG BUY","BUY"
    elif score>=70: signal,side="BUY WATCH","BUY"
    elif score<=14: signal,side="STRONG SELL","SELL"
    elif score<=30: signal,side="SELL WATCH","SELL"
    else: signal,side="WAIT","WAIT"
    stop_dist=max(av*1.5,price*0.01); t1_dist=max(av*2.2,price*0.014); t2_dist=max(av*3.4,price*0.022)
    if side=="SELL": stop=price+stop_dist; t1=price-t1_dist; t2=price-t2_dist
    else: stop=price-stop_dist; t1=price+t1_dist; t2=price+t2_dist
    rr=abs(t1-price)/abs(price-stop) if abs(price-stop)>0 else 0
    risk="LOW" if av/price<0.025 else "MEDIUM" if av/price<0.055 else "HIGH"
    reasons=(bull if side=="BUY" else bear) if side!="WAIT" else (bull+bear)
    reason="; ".join(reasons[:9])
    ai=explain(meta["name"],side,signal,score,risk,round(rr,2),reason)
    return {"symbol":None,"name":meta["name"],"ticker":meta["ticker"],"group":meta["group"],"price":round(price,4),
            "signal":signal,"side":side,"confidence":int(score),"risk":risk,"trend":"BULLISH" if e50>e200 else "BEARISH",
            "rsi":round(rv,2),"atr":round(av,4),"macd":round(mv,4),"bollinger_pos":round(bb,2),"candle":pat,
            "ema8":round(e8,4),"ema20":round(e20,4),"ema50":round(e50,4),"ema100":round(e100,4),"ema200":round(e200,4),
            "change_5d":round(ch5,2),"change_20d":round(ch20,2),"change_60d":round(ch60,2),
            "entry":round(price,4),"stop":round(stop,4),"target1":round(t1,4),"target2":round(t2,4),"risk_reward":round(rr,2),
            "reason":reason,"ai_explanation":ai,"updated_at":now_iso()}

