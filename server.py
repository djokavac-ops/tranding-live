def update_open(snap):
    for t in open_trades():
        x=snap.get(t['symbol'])
        if not x or x.get('error'): continue
        price=float(x['price']); side=t['side']; pnl=(price-t['entry'])*t['qty'] if side=='BUY' else (t['entry']-price)*t['qty']; status='OPEN'
        if side=='BUY' and price<=t['stop']: status='STOP_LOSS'
        if side=='BUY' and price>=t['target1']: status='TAKE_PROFIT'
        if side=='SELL' and price>=t['stop']: status='STOP_LOSS'
        if side=='SELL' and price<=t['target1']: status='TAKE_PROFIT'
        q('UPDATE paper_trades SET current_price=:p,pnl=:pnl,status=:st,closed_at=:cl WHERE id=:id',{'p':price,'pnl':round(pnl,2),'st':status,'cl':now() if status!='OPEN' else None,'id':t['id']})
        if status!='OPEN': send_telegram(f"Paper close {status}: {t['side']} {t['name']} PnL {round(pnl,2)} EUR")
def best_candidate(snap,s):
    opened={t['symbol'] for t in open_trades()}; arr=[]
    for sym,x in snap.items():
        if x.get('error') or sym in opened: continue
        if x['signal'] not in ('STRONG BUY','STRONG SELL'): continue
        if x['confidence']<s['min_confidence'] or x['risk_reward']<s['min_risk_reward']: continue
        if x['risk']=='HIGH' and x['confidence']<90: continue
        arr.append(x)
    arr.sort(key=lambda x:(x['confidence'],x['risk_reward']),reverse=True); return arr[0] if arr else None
def engine_log(st,op,msg): q('INSERT INTO engine_runs(ts,status,opened_trade,message) VALUES(:ts,:s,:o,:m)',{'ts':now(),'s':st,'o':1 if op else 0,'m':msg})
def run_cycle():
    with LOCK:
        s=settings(); snap=market(True); update_open(snap); p=pnl_today(); tt=trades_today(); ot=open_trades()
        for cond,msg in [(p>=s['daily_target'],'STOP: daily target reached'),(p<=-s['daily_max_loss'],'STOP: daily max loss reached'),(len(tt)>=int(s['max_trades_per_day']),'STOP: max trades reached'),(len(ot)>=int(s['max_open_trades']),'STOP: max open trades reached')]:
            if cond: engine_log('STOP',False,msg); return {'executed':False,'status':msg,'daily_pnl':p}
        c=best_candidate(snap,s)
        if not c: msg='No qualified professional setup'; engine_log('WAIT',False,msg); return {'executed':False,'status':msg,'daily_pnl':p}
        qty=(s['account_size']*s['risk_per_trade_pct']/100)/abs(c['entry']-c['stop'])
        q('INSERT INTO paper_trades(day,opened_at,closed_at,symbol,name,group_name,side,entry,current_price,stop,target1,target2,qty,confidence,risk_reward,status,pnl,reason,ai_explanation) VALUES(:day,:oa,:ca,:sym,:name,:grp,:side,:entry,:cur,:stop,:t1,:t2,:qty,:conf,:rr,:status,:pnl,:reason,:ai)',{'day':day(),'oa':now(),'ca':None,'sym':c['symbol'],'name':c['name'],'grp':c['group'],'side':c['side'],'entry':c['entry'],'cur':c['price'],'stop':c['stop'],'t1':c['target1'],'t2':c['target2'],'qty':qty,'conf':c['confidence'],'rr':c['risk_reward'],'status':'OPEN','pnl':0,'reason':c['reason'],'ai':c['ai_explanation']})
        msg=f"Paper open: {c['side']} {c['name']} @ {c['entry']} | SL {c['stop']} | TP1 {c['target1']} | conf {c['confidence']}%"; send_telegram(msg); engine_log('OPENED',True,msg); return {'executed':True,'status':msg,'candidate':c,'daily_pnl':pnl_today()}
def report(trades,title):
    closed=[t for t in trades if t['status']!='OPEN']; wins=[t for t in closed if float(t['pnl'])>0]; losses=[t for t in closed if float(t['pnl'])<0]; pnl=round(sum(float(t['pnl']) for t in trades),2); gw=sum(float(t['pnl']) for t in wins); gl=abs(sum(float(t['pnl']) for t in losses)); by={}
    for t in trades: by[t['symbol']]=by.get(t['symbol'],0)+float(t['pnl'])
    top=max(by.items(),key=lambda x:x[1],default=None); best=max(trades,key=lambda x:float(x['pnl']),default=None); worst=min(trades,key=lambda x:float(x['pnl']),default=None)
    return {'title':title,'total_trades':len(trades),'closed_trades':len(closed),'open_trades':len(trades)-len(closed),'wins':len(wins),'losses':len(losses),'win_rate':round(len(wins)/len(closed)*100,1) if closed else 0,'profit_factor':round(gw/gl,2) if gl>0 else 0,'pnl':pnl,'best_trade':dict(best) if best else None,'worst_trade':dict(worst) if worst else None,'top_symbol':{'symbol':top[0],'pnl':round(top[1],2)} if top else None,'comment':'Paper rezultat je pozitivan.' if pnl>0 else 'Paper rezultat je negativan ili neutralan.'}
