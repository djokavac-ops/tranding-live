def chart(ticker):
    r=requests.get(f'https://query1.finance.yahoo.com/v8/finance/chart/{ticker}',params={'range':'1y','interval':'1d'},headers={'User-Agent':'Mozilla/5.0'},timeout=15); r.raise_for_status(); res=r.json().get('chart',{}).get('result')
    if not res: raise ValueError('No market data')
    d=res[0]; qu=d['indicators']['quote'][0]; rows=[]
    for i,ts in enumerate(d['timestamp']):
        o,h,l,c=qu['open'][i],qu['high'][i],qu['low'][i],qu['close'][i]; v=qu.get('volume',[0]*len(d['timestamp']))[i]
        if None in (o,h,l,c): continue
        rows.append({'open':float(o),'high':float(h),'low':float(l),'close':float(c),'volume':float(v or 0)})
    if len(rows)<90: raise ValueError('Not enough history')
    return rows
def ema(a,n):
    alpha=2/(n+1); out=a[0]
    for v in a[1:]: out=alpha*v+(1-alpha)*out
    return out
def sma(a,n):
    b=a[-n:] if len(a)>=n else a; return sum(b)/len(b) if b else 0
def rsi(a,n=14):
    g=[]; l=[]
    for i in range(-n,0):
        ch=a[i]-a[i-1]; g.append(max(ch,0)); l.append(abs(min(ch,0)))
    ag=sum(g)/n; al=sum(l)/n; return 100 if al==0 else 100-100/(1+ag/al)
def atr(rows,n=14):
    trs=[]; prev=rows[-n-1]['close']
    for row in rows[-n:]: trs.append(max(row['high']-row['low'],abs(row['high']-prev),abs(row['low']-prev))); prev=row['close']
    return sum(trs)/len(trs)
def pct(a,n): return 0 if len(a)<=n else (a[-1]/a[-n-1]-1)*100
def analyze(rows,meta):
    c=[x['close'] for x in rows]; v=[x['volume'] for x in rows]; price=c[-1]; e8=ema(c[-60:],8); e20=ema(c[-80:],20); e50=ema(c[-160:],50); e200=ema(c,200); rv=rsi(c); av=atr(rows); mac=ema(c[-120:],12)-ema(c[-140:],26); ch5=pct(c,5); ch20=pct(c,20); ch60=pct(c,60); vol=sma(v,20); vf=v[-1]/vol if vol>0 else 1
    raw=50; bull=[]; bear=[]
    if price>e20: raw+=6; bull.append('price above EMA20')
    else: raw-=6; bear.append('price below EMA20')
    if e8>e20>e50: raw+=10; bull.append('bullish EMA stack')
    elif e8<e20<e50: raw-=10; bear.append('bearish EMA stack')
    if e50>e200: raw+=12; bull.append('primary trend bullish')
    else: raw-=12; bear.append('primary trend bearish')
    if mac>0: raw+=6; bull.append('MACD positive')
    else: raw-=6; bear.append('MACD negative')
    if 50<=rv<=66: raw+=8; bull.append('RSI healthy')
    elif rv>75: raw-=9; bear.append('RSI overbought')
    elif rv<35: raw-=9; bear.append('RSI weak')
    if ch5>0 and ch20>0: raw+=8; bull.append('5d and 20d momentum positive')
    elif ch5<0 and ch20<0: raw-=8; bear.append('5d and 20d momentum negative')
    raw += 5 if ch60>0 else -5
    if vf>1.4: raw+=3; bull.append('volume expansion')
    score=max(0,min(96,round(raw)))
    if score>=86: signal,side='STRONG BUY','BUY'
    elif score>=70: signal,side='BUY WATCH','BUY'
    elif score<=14: signal,side='STRONG SELL','SELL'
    elif score<=30: signal,side='SELL WATCH','SELL'
    else: signal,side='WAIT','WAIT'
    sd=max(av*1.5,price*.01); t1=max(av*2.2,price*.014); t2=max(av*3.4,price*.022)
    stop,target1,target2=(price+sd,price-t1,price-t2) if side=='SELL' else (price-sd,price+t1,price+t2)
    rr=abs(target1-price)/abs(price-stop) if abs(price-stop)>0 else 0; risk='LOW' if av/price<.025 else 'MEDIUM' if av/price<.055 else 'HIGH'; reason='; '.join((bull if side=='BUY' else bear) if side!='WAIT' else bull+bear)
    ai=f"{meta['name']}: {'WAIT' if side=='WAIT' else 'sistem bira '+('kupovinu' if side=='BUY' else 'prodaju')} | confidence {score}% | risk {risk} | RR 1:{round(rr,2)} | {reason}"
    return {'symbol':None,'name':meta['name'],'ticker':meta['ticker'],'group':meta['group'],'price':round(price,4),'signal':signal,'side':side,'confidence':int(score),'risk':risk,'trend':'BULLISH' if e50>e200 else 'BEARISH','rsi':round(rv,2),'atr':round(av,4),'macd':round(mac,4),'ema8':round(e8,4),'ema20':round(e20,4),'ema50':round(e50,4),'ema200':round(e200,4),'change_5d':round(ch5,2),'change_20d':round(ch20,2),'change_60d':round(ch60,2),'entry':round(price,4),'stop':round(stop,4),'target1':round(target1,4),'target2':round(target2,4),'risk_reward':round(rr,2),'reason':reason,'ai_explanation':ai,'candle':'N/A','updated_at':now()}
