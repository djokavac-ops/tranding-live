def analytics_from_trades(trades, title="Performance"):
    closed=[t for t in trades if t.get("status")!="OPEN"]
    wins=[t for t in closed if float(t.get("pnl") or 0)>0]
    losses=[t for t in closed if float(t.get("pnl") or 0)<0]
    pnl_values=[float(t.get("pnl") or 0) for t in closed]
    total_pnl=round(sum(float(t.get("pnl") or 0) for t in trades),2)
    gross_win=sum(float(t.get("pnl") or 0) for t in wins)
    gross_loss=abs(sum(float(t.get("pnl") or 0) for t in losses))
    avg_win=round(gross_win/len(wins),2) if wins else 0
    avg_loss=round(gross_loss/len(losses),2) if losses else 0
    expectancy=round((len(wins)/len(closed))*avg_win - (len(losses)/len(closed))*avg_loss,2) if closed else 0
    profit_factor=round(gross_win/gross_loss,2) if gross_loss else 0
    win_rate=round(len(wins)/len(closed)*100,1) if closed else 0

    equity=[]; running=0; peak=0; max_dd=0
    for t in sorted(closed, key=lambda x: x.get("id",0)):
        running += float(t.get("pnl") or 0)
        peak=max(peak,running)
        dd=peak-running
        max_dd=max(max_dd,dd)
        equity.append({"trade_id":t.get("id"),"equity":round(running,2),"drawdown":round(dd,2)})

    by_strategy={}
    by_symbol={}
    for t in closed:
        st=t.get("strategy") or "unknown"
        sy=t.get("symbol") or "unknown"
        by_strategy.setdefault(st,{"trades":0,"pnl":0,"wins":0})
        by_symbol.setdefault(sy,{"trades":0,"pnl":0,"wins":0})
        for d in (by_strategy[st],by_symbol[sy]):
            d["trades"]+=1
            d["pnl"]+=float(t.get("pnl") or 0)
            if float(t.get("pnl") or 0)>0: d["wins"]+=1
    for d in list(by_strategy.values())+list(by_symbol.values()):
        d["pnl"]=round(d["pnl"],2)
        d["win_rate"]=round(d["wins"]/d["trades"]*100,1) if d["trades"] else 0

    return {
        "title":title,
        "total_trades":len(trades),
        "closed_trades":len(closed),
        "open_trades":len(trades)-len(closed),
        "pnl":total_pnl,
        "win_rate":win_rate,
        "profit_factor":profit_factor,
        "avg_win":avg_win,
        "avg_loss":avg_loss,
        "expectancy":expectancy,
        "max_drawdown":round(max_dd,2),
        "equity_curve":equity[-100:],
        "by_strategy":by_strategy,
        "by_symbol":by_symbol,
    }

def portfolio_health():
    s=get_settings()
    open_pos=open_trades()
    account=float(s.get("account_size",0) or 0)
    total_risk=0
    exposure_by_group={}
    exposure_by_symbol={}
    for t in open_pos:
        entry=float(t.get("entry") or 0)
        stop=float(t.get("stop") or 0)
        qty=float(t.get("qty") or 0)
        risk=abs(entry-stop)*qty
        notional=abs(entry*qty)
        total_risk += risk
        group=t.get("group_name") or "unknown"
        symbol=t.get("symbol") or "unknown"
        exposure_by_group[group]=round(exposure_by_group.get(group,0)+notional,2)
        exposure_by_symbol[symbol]=round(exposure_by_symbol.get(symbol,0)+notional,2)
    risk_pct=round(total_risk/account*100,2) if account else 0
    status="OK"
    warnings=[]
    if risk_pct>3:
        status="CAUTION"; warnings.append("Portfolio risk above 3%")
    if len(open_pos)>=int(s.get("max_open_trades",2)):
        warnings.append("Max open positions reached")
    return {"status":status,"open_positions":len(open_pos),"total_risk_eur":round(total_risk,2),"total_risk_pct":risk_pct,"exposure_by_group":exposure_by_group,"exposure_by_symbol":exposure_by_symbol,"warnings":warnings}

